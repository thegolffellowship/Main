/**
 * =============================================================================
 * SUPABASE MIDDLEWARE CLIENT
 * =============================================================================
 * Used in Next.js middleware to handle session refresh.
 *
 * WHAT IS MIDDLEWARE?
 * Middleware runs BEFORE every page request. It can:
 * - Check if user is logged in
 * - Redirect unauthorized users
 * - Refresh expired sessions
 *
 * This ensures that when a user visits any page, their session is always fresh.
 * =============================================================================
 */

import { createServerClient, type CookieOptions } from '@supabase/ssr';
import { NextResponse, type NextRequest } from 'next/server';

export async function updateSession(request: NextRequest) {
  let supabaseResponse = NextResponse.next({
    request,
  });

  const supabase = createServerClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,
    {
      cookies: {
        getAll() {
          return request.cookies.getAll();
        },
        setAll(cookiesToSet) {
          cookiesToSet.forEach(({ name, value, options }) =>
            request.cookies.set(name, value)
          );
          supabaseResponse = NextResponse.next({
            request,
          });
          cookiesToSet.forEach(({ name, value, options }) =>
            supabaseResponse.cookies.set(name, value, options)
          );
        },
      },
    }
  );

  // IMPORTANT: DO NOT REMOVE
  // This refreshes the session if expired - required for Server Components
  // https://supabase.com/docs/guides/auth/server-side/nextjs
  const {
    data: { user },
  } = await supabase.auth.getUser();

  return { supabaseResponse, user };
}
